-- Status bars and logging

-- Perform an arbitrary action on each internal state change or X event.
-- See the 'XMonad.Hooks.DynamicLog' extension for examples.
--
myLogHook proc proc2 = dynamicLogWithPP $ xmobarPP
				{ ppOutput = \x -> hPutStrLn proc x >> hPutStrLn proc2 x
				, ppCurrent = xmobarColor "#88dd88" "" . wrap "[" "]"
				, ppVisible = xmobarColor "#88dd88" ""
				, ppHidden = xmobarColor "#f3a712" "" . wrap "" ""
				, ppHiddenNoWindows = xmobarColor "#dcdfe4" ""
				, ppTitle = xmobarColor "#dcdfe4" "" . shorten 60
				, ppSep = " | "
				, ppUrgent = xmobarColor "#fe5e41" "" . wrap "!" "!"
				, ppOrder = \(ws:l:t:ex) -> [ws,l]++ex++[t]
				}


